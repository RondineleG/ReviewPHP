<?php

$value = 20;

//pre-increment
echo  ++$value;
echo"<hr>";
echo $value;
echo"<hr>";



//pos-increment
echo  $value++;
echo"<hr>";
echo $value;
echo"<hr>";

//pre-decrement
echo  --$value;
echo"<hr>";
echo $value;
echo"<hr>";

//pos-decrement
echo  $value--;
echo"<hr>";
echo $value;
echo"<hr>";